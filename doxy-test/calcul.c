/** Calcul la somme de deux entiers
 * @return \f$a+b\f$
 */
int addition(int a, int b)
{
  return a + b;
}
/** Calcul le produit de deux entiers
 * @return \f$a*b\f$
 */
int multiplication(int a, int b)
{
  return a * b;
}

/** Calcul la norme d'un vecteur
 * @return \f$\sqrt{(x_2-x_1)^2+(y_2-y_1)^2}\f$
 */
double norme(double x1, double y1, double x2, double y2)
{
  return sqrt( pow(x2-x1,2) + pow(y2-y1,2) );
}
